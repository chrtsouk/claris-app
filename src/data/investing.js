export const getInvestingToolsBySubcat = (sub) =>
  INVESTING_TOOLS.filter(t => sub === 'all' || t.subcategory === sub)

export const getInvestingToolById = (id) =>
  INVESTING_TOOLS.find(t => t.id === id)

export const searchInvestingTools = (query, sub = 'all') => {
  const q = query.toLowerCase()
  return INVESTING_TOOLS.filter(t =>
    (sub === 'all' || t.subcategory === sub) &&
    (t.name.toLowerCase().includes(q) ||
     t.subcategory.toLowerCase().includes(q) ||
     t.type.toLowerCase().includes(q) ||
     t.users.some(u => u.toLowerCase().includes(q)))
  )
}
