export const getBizToolsBySubcat = (sub) =>
  BUSINESS_TOOLS.filter(t => sub === 'all' || t.subcategory === sub)

export const getBizToolById = (id) =>
  BUSINESS_TOOLS.find(t => t.id === id)

export const searchBizTools = (query, sub = 'all') => {
  const q = query.toLowerCase()
  return BUSINESS_TOOLS.filter(t =>
    (sub === 'all' || t.subcategory === sub) &&
    (t.name.toLowerCase().includes(q) ||
     t.subcategory.toLowerCase().includes(q) ||
     t.type.toLowerCase().includes(q) ||
     t.users.some(u => u.toLowerCase().includes(q)))
  )
}
